function WPATH(s) {
    var index = s.lastIndexOf("/");
    var path = -1 === index ? "npm/" + s : s.substring(0, index) + "/npm/" + s.substring(index + 1);
    return true && 0 !== path.indexOf("/") ? "/" + path : path;
}

function __processArg(obj, key) {
    var arg = null;
    if (obj) {
        arg = obj[key] || null;
        delete obj[key];
    }
    return arg;
}

